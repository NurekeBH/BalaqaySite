window.CURRENT_LANG = localStorage.getItem('lang') || 'zh';

window.t = function (key, params) {
  const dict = window.I18N[window.CURRENT_LANG] || window.I18N.zh;
  let s = dict[key];
  if (s == null) s = (window.I18N.zh[key] != null ? window.I18N.zh[key] : key);
  if (params) {
    for (const k in params) s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
  }
  return s;
};

window.setLang = function (lang) {
  window.CURRENT_LANG = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang;
  window.applyTranslations();
  if (window.refresh) window.refresh();
  // persist to backend
  fetch((location.origin.startsWith('http') ? '' : 'http://localhost:3001') + '/api/settings/lang', {
    method: 'PUT',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ value: lang }),
  }).catch(() => {});
};

// Apply translations to all elements with data-i18n / data-i18n-ph / data-i18n-title
window.applyTranslations = function () {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.innerHTML = window.t(key);
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    el.setAttribute('placeholder', window.t(key));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.setAttribute('title', window.t(key));
  });
};
